(function(global){
    $(document).on('click','.schema-list',function(e){
        e.preventDefault();
        console.log('we enter schemalist-'+e);
        $.ajax({
            type: 'GET',
            URL:'/getschema',
            data: $(this).url,
            success: function(sdata){
                console.log('get schema!');
            },
            error: function(e){
                console.log(e);
            }
        });
    });
}(window))